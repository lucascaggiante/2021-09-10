	public List<ArcoGrafo> calcolaArchi(String city) {
		String sql = "SELECT b1.business_id as id1, b2.business_id as id2, b1.latitude AS x1, b1.longitude AS y1, b2.latitude AS x2, b2.longitude AS y2\n"
				+ "FROM business b1, business b2\n"
				+ "WHERE b1.business_id > b2.business_id\n"
				+ "	AND b1.city = b2.city\n"
				+ "	AND b1.city = ?" ;
		
		List<ArcoGrafo> result = new ArrayList<>() ;
		
		Connection conn = DBConnect.getConnection() ;
		
		try {
			PreparedStatement st = conn.prepareStatement(sql) ;
			st.setString(1, city);
			ResultSet res = st.executeQuery() ;
			while(res.next()) {
				Double x1 = res.getDouble("x1");
				Double y1 = res.getDouble("y1");
				Double x2 = res.getDouble("x2");
				Double y2 = res.getDouble("y2");
				
				Double peso = LatLngTool.distance(new LatLng(x1,y1), 
						new LatLng(x2, y2), 
						LengthUnit.KILOMETER);
				
				result.add(new ArcoGrafo(
						res.getString("id1"), 
						res.getString("id2"), 
						peso)) ;
			}
			conn.close();
			return result ;
		} catch(SQLException ex) {
			throw new RuntimeException("DB Error", ex) ;
		}
	}
	
